//Comparação maior e menor

const idadeGuada = 30
const idadeMaria = 40

console.log("Guada é mais velha que Maria? ", idadeGuada > idadeMaria) //false
console.log("Maria é mais nova que Guada? ", idadeMaria < idadeGuada)  //false

//Comparação maior ou igual e menor ou igual

